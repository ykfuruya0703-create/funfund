const FF_STORAGE_KEYS = {
  CUSTOM: 'ff_custom_columns_v1',
  DELETED: 'ff_deleted_ids_v1'
};

function ffEscHtml(s){
  return String(s == null ? '' : s).replace(/[&<>"']/g, c => ({
    '&':'&amp;', '<':'&lt;', '>':'&gt;', '"':'&quot;', "'":'&#39;'
  }[c]));
}

function ffLoadCustom(){
  try { return JSON.parse(localStorage.getItem(FF_STORAGE_KEYS.CUSTOM)) || []; }
  catch(e){ return []; }
}
function ffSaveCustom(list){
  localStorage.setItem(FF_STORAGE_KEYS.CUSTOM, JSON.stringify(list));
}
function ffLoadDeleted(){
  try { return JSON.parse(localStorage.getItem(FF_STORAGE_KEYS.DELETED)) || []; }
  catch(e){ return []; }
}
function ffSaveDeleted(list){
  localStorage.setItem(FF_STORAGE_KEYS.DELETED, JSON.stringify(list));
}

/* シードデータ + 管理画面からの追加/編集/削除をマージして返す（日付の新しい順） */
function ffGetAllColumns(){
  const custom = ffLoadCustom();
  const deleted = new Set(ffLoadDeleted());
  const customIds = new Set(custom.map(c => c.id));
  const seed = FF_SEED_COLUMNS.filter(c => !deleted.has(c.id) && !customIds.has(c.id));
  const merged = [...custom, ...seed];
  merged.sort((a, b) => String(b.date||'').localeCompare(String(a.date||'')));
  return merged;
}

