function ffUpsertColumn(col){
  const custom = ffLoadCustom();
  const idx = custom.findIndex(c => c.id === col.id);
  if(idx >= 0) custom[idx] = col; else custom.unshift(col);
  ffSaveCustom(custom);
}

/* 管理画面からの追加分を削除、シード記事の場合は非表示リストへ */
function ffDeleteColumn(id){
  let custom = ffLoadCustom();
  custom = custom.filter(c => c.id !== id);
  ffSaveCustom(custom);
  const deleted = ffLoadDeleted();
  if(!deleted.includes(id)){
    deleted.push(id);
    ffSaveDeleted(deleted);
  }
}

function ffGenerateId(){
  const rand = Math.random().toString(36).slice(2, 7);
  const stamp = Date.now().toString(36);
  return `col-${stamp}-${rand}`;
}

function ffVisual(col){
  const pal = FF_CAT_PALETTE[col.catClass] || FF_CAT_PALETTE.gold;
  return {
    mono: col.mono || (col.title ? col.title.trim().charAt(0) : '文'),
    c1: col.c1 || pal.c1,
    c2: col.c2 || pal.c2
  };
}

/* カード/モーダル共通の画像(またはプレースホルダー)HTMLを生成 */
function ffPhImgHTML(col){
  const v = ffVisual(col);
  const style = `--c1:${v.c1};--c2:${v.c2};`;
  const cat = ffEscHtml(col.category || '');
  if(col.image){
    return `<div class="ph-img" style="${style}"><img class="real-img" src="${ffEscHtml(col.image)}" alt="${ffEscHtml(col.title || '')}"><span class="ph-cat" style="position:absolute;bottom:0;left:0">${cat}</span></div>`;
  }
  return `<div class="ph-img" style="${style}" data-mono="${ffEscHtml(v.mono)}"><span class="ph-cat">${cat}</span></div>`;
}
